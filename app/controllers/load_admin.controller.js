const db = require("../models");
const Load = db.loads;
const Project = db.projects;
const Load_Admin = db.load_admins;
const User = db.users;
const Op = db.Sequelize.Op;

exports.create = (req, res) => {
 
    // Crear un Rol
    const load = {
      type: req.body.type,
      initial_date: req.body.initial_date,
      final_date: req.body.final_date,
      hours: req.body.hours,
      observations: req.body.observations,
      user_id: req.body.user
    };
  
    // Guardar Rol en BD
    Load_Admin.create(load)
      .then(data => {
        res.send(data);
      })
      .catch(err => {
        res.status(500).send({
          message:
            err.message || "Ocurrió un error mientras se guardaba el rol en base de datos."
        });
      });
  };

// Listar todos los users
exports.findAll = (req, res) => {
    Load_Admin.findAll({
      include: [{
        model: User,
        as: "user",
        attributes: ["id", "name", "lastname", "username"],
      },
      ]
    })
      .then(data => {
        res.send(data);
      })
      .catch(err => {
        res.status(500).send({
          message:
            err.message || "Ocurrió un error mientras se consultaba en base de datos."
        });
      });
  };


// Buscar un user por ID
exports.findOne = (req, res) => {
        const id = req.params.id;
      
        Load_Admin.findByPk(id, { 
            include: [{
                model: User,
                as: "user",
                attributes: ["id", "name", "lastname", "username"],
              },
              ]
    })
          .then(data => {
            res.send(data);
          })
          .catch(err => {
            res.status(500).send({
              message: "Ocurrió un error mientras se consultaba el User " + id
            });
          });
};

// Actualizar un User por ID
exports.update = (req, res) => {
    const id = req.params.id;

    Load_Admin.update(req.body, {
      where: { id: id }
    })
      .then(num => {
        if (num == 1) {
          res.send({
            message: "User actualizado correctamente."
          });
        } else {
          res.send({
            message: `No se pudo actualizar el User con el id=${id}. Es posible que el User no haya sido encontraodo o la petición esté vacía!`
          });
        }
      })
      .catch(err => {
        res.status(500).send({
          message: "Ocurrió un error mientras se actualizaba el User " + id
        });
      });
};

// Eliminar un User por ID
exports.delete = (req, res) => {
    const id = req.params.id;
  
    Load_Admin.destroy({
      where: { id: id }
    })
      .then(num => {
        if (num == 1) {
          res.send({
            message: "User eliminado correctamente!"
          });
        } else {
          res.send({
            message: `No se pudo eliminar el User con el id=${id}. Es posible que el User no haya sido encontraodo o la petición esté vacía!`
          });
        }
      })
      .catch(err => {
        res.status(500).send({
          message: "Ocurrió un error mientras se actualizaba el User " + id
        });
      });
  };

